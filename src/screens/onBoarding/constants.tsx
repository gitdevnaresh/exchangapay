export interface FormValues {
    firstName: string;
    lastName: string;
    dateOfBirth: Date | null;
};

export const userValidationSchema = Yup.object().shape({
    firstName: Yup.string()
        .required(USER_CONSTANTS?.REQUIRED)
        .min(2, USER_CONSTANTS?.FIRST_NAME_MIN)
        .matches(/^[a-zA-Z]+( [a-zA-Z]+)*$/, USER_CONSTANTS?.FIRST_NAME_CHARACTERS),
    lastName: Yup.string()
        .required(USER_CONSTANTS?.REQUIRED)
        .min(2, USER_CONSTANTS?.LAST_NAME_MIN)
        .matches(/^[a-zA-Z]+( [a-zA-Z]+)*$/, USER_CONSTANTS?.LAST_NAME_CHARACTERS),
    dateOfBirth: Yup.date()
        .nullable()
        .required(USER_CONSTANTS?.REQUIRED)
        .test("age", USER_CONSTANTS.AGE_VALIDATION, (value) => {
            if (!value) return false;
            const today = new Date();
            const birthDate = new Date(value);
            const age = today.getFullYear() - birthDate.getFullYear();
            return age >= 18;
        }),
});



